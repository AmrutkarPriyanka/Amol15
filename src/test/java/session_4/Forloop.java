package session_4;

public class Forloop {
	
	public static void main(String[] args) {
		
		for(int i=0; i<=10;i++)
		{
			System.out.println(i);
		}
		
		System.out.println("-----------------------------------");

		for(int j=10;j>=0;j--)
			
		{
			System.out.println(j);

			
			if(j==3)
				
				
			{
				System.out.println("I got number three");
				break;
			
		}
		}
		
	}

}
