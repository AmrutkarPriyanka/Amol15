package session_2;

public class CharacterConcatenation {
	
	
public static void main(String[] args) {
		
		char c1 = '5';  int z1 = 5;
		char c2 = '4';  int z2 = 4;
		char c3 = 'A';  String q1 = "A"; 
		char c4 = 'B';  String q2 = "B";
		
		
        System.out.println(c1+c2);  //See ASCII value for output //105
        
        System.out.println(z1+z2);  //9
        
        System.out.println(c3+c4);  //131
        
        System.out.println(q1+q2);  //AB
		
		
		
	}

}
